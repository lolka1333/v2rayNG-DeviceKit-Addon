    private class RsaPrivateKey(modulusBytes: ByteArray, privateExponentBytes: ByteArray) {
        val modulus: BigInteger = BigInteger(1, modulusBytes)
        val privateExponent: BigInteger = BigInteger(1, privateExponentBytes)
        val keySizeBytes: Int = (modulus.bitLength() + 7) / 8
    }

    private val rsaPkcs1v15PrivateKeys: List<RsaPrivateKey> by lazy {
        listOf(
            RsaPrivateKey(
                Crypt4KeyMaterial.modulusBytes,
                Crypt4KeyMaterial.privateExponentBytes
            )
        )
    }

    fun tryDecrypt(input: String?): String? {
        try {
            if (input.isNullOrBlank()) return null

            var raw = input.trim()
            if (raw.startsWith(PREFIX)) {
                raw = Uri.decode(raw)
            }
            if (!raw.startsWith(PREFIX)) return null

            val slash = raw.indexOf('/', PREFIX.length)
            if (slash <= 0) return null

            val host = raw.substring(PREFIX.length, slash)
            val payload = raw.substring(slash + 1)

            val rsaPrivateKeys = when (host) {
                "crypt4" -> rsaPkcs1v15PrivateKeys
                else -> emptyList()
            }
            if (rsaPrivateKeys.isEmpty()) return null

            val cleaned = payload
                .replace(' ', '+')
                .replace(Regex("[^A-Za-z0-9+/=_-]"), "")
                .replace('-', '+')
                .replace('_', '/')
                .trimEnd('=')

            val maxCiphertextSizeBytes = rsaPrivateKeys.maxOf { it.keySizeBytes }
            val ciphertextBytes = findCiphertextBytes(cleaned, maxCiphertextSizeBytes) ?: return null
            val ciphertextInt = BigInteger(1, ciphertextBytes)

            for (privateKey in rsaPrivateKeys) {
                val encodedMessageInt = ciphertextInt.modPow(privateKey.privateExponent, privateKey.modulus)
                val encodedMessageBytes = toFixedLength(encodedMessageInt, privateKey.keySizeBytes)
                val plaintextBytes = pkcs1v15Unpad(encodedMessageBytes) ?: continue

                val decoder = Charsets.UTF_8
                    .newDecoder()
                    .onMalformedInput(CodingErrorAction.IGNORE)
                    .onUnmappableCharacter(CodingErrorAction.IGNORE)

                return decoder.decode(ByteBuffer.wrap(plaintextBytes)).toString()
            }

            return null
        } catch (_: Exception) {
            return null
        }
    }

    private fun pkcs1v15Unpad(block: ByteArray): ByteArray? {
        if (block.size < 11) return null
        if (block[0] != 0.toByte() || block[1] != 2.toByte()) return null

        var sep = -1
        for (i in 2 until block.size) {
            if (block[i] == 0.toByte()) {
                sep = i
                break
            }
        }
        if (sep < 10) return null
        for (i in 2 until sep) {
            if (block[i] == 0.toByte()) return null
        }

        return block.copyOfRange(sep + 1, block.size)
    }

    private fun findCiphertextBytes(body: String, maxSizeBytes: Int): ByteArray? {
        for (c in 0..8) {
            var t = if (c == 0) body else body.dropLast(c)
            while (t.length % 4 == 1 && t.isNotEmpty()) {
                t = t.dropLast(1)
            }
            if (t.isEmpty()) continue

            val padLen = (4 - (t.length % 4)) % 4
            val padded = t + "=".repeat(padLen)

            val decoded = try {
                Base64.decode(padded, Base64.NO_WRAP)
            } catch (_: Exception) {
                continue
            }

            if (decoded.isNotEmpty() && decoded.size <= maxSizeBytes) {
                return decoded
            }
            if (decoded.size == maxSizeBytes + 1 && decoded[0] == 0.toByte()) {
                return decoded.copyOfRange(1, decoded.size)
            }
        }
        return null
    }

    private fun toFixedLength(value: BigInteger, length: Int): ByteArray {
        val raw = value.toByteArray()

        if (raw.size == length) return raw
        if (raw.size == length + 1 && raw[0] == 0.toByte()) {
            return raw.copyOfRange(1, raw.size)
        }
        if (raw.size < length) {
            return ByteArray(length - raw.size) + raw
        }
        return raw.copyOfRange(raw.size - length, raw.size)
    }
